amostra randomica = pegar dados aleatorios
amostra estraitifcada = dados por regiões diversificadas

distribuiçao normal = a media da amostra é igual a media da populaçao
graus de liberdade = n-1 faz sentido quando a amostra é pequena, porem com muitos dados dá para descartar o -1 pois os dados tendem a serem muito iguais

graus de confiança = tenho 95% de certeza que a media esta entre x e y
quanto maior o nivel de confiança, maior a disperçao dos dados
