variabilidade, dispersão de dados, outliers e quartis

amplitude = o maior menos o menor (pode conter distorçoes grandes)

distribuição de quartis
retira 25% de dadods de cada extremidade e deixa os 50% do grupo do meio
Exemplo 3,3,6,7,7,10,10,10,11,13,30 
Para descrubrir os quartis, faço a média da quantia de dados (ex. 11/4) =~3
Conto 3 numero a partir do inicio
3,3,6 (descarto todos que estiverem do lado do esquerdo do 6 - (ex. 3,3))
para descobrir a outra extremidade faço (3*11)/4 = 33/4 = 8,... = 9
Conto 9 dados da esq >> dir. ex. 11,13,30
Tudo que ficou após o 11 é desconsiderado (13,30)
O que sobrou são os 50% necessários (6,7,7,10,10,10,11)

Após descobrir o quartil é possivel visualizar o BOXPLOT
em um gráfico cartesiano, em algum eixo coloco 
o primeiro dado 3
o primeiro quartil 6
o segundo quartil 11
o ultimo dado 30
Está feito o boxplot

